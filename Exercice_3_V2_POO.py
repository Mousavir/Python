from math import pi

#classe cercle qui calcul air cerlce aver valeurs de l'air cercle et son circomference avec nombre pour rayon predefinit
class Cercle:
    #fonction calculer air cerlce avec formule, nombre pi et valeur definit rayon cercle
    def calcul_air(self):
        return pi * 5 ** 2

    # fonction calculer circomference cerlce avec formule, nombre pi et valeur definit rayon cercle
    def calcul_circonference(self):
        return 2 * pi * 5